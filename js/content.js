function splitTitle(element, str, ti, date) {

    element.innerHTML = '';
    str = str.split(' ');
    var l = str.length;

    // for each word //
    for (var i=0; i<l; i++) {
        var wrap = document.createElement('span');
        wrap.classList = 'title-anim-wrap';
        var inner = document.createElement('span');
        inner.innerHTML = str[i];
        inner.classList = 'title-anim';
        inner.style.transitionDelay = '' + (0.15 * (i + ti)) + 's';

        wrap.appendChild(inner);
        element.appendChild(wrap);

        if (i < (l-1)) {
            var space = document.createTextNode(' ');
            element.appendChild(space);
        } else {
            if (date) {
                var grammar = document.createTextNode('.');
                inner.appendChild(grammar);
            }

        }
    }
    return ti + l;
}

